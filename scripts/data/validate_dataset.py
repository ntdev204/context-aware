"""Dataset Quality Gate.

Reads the JSON report generated by explore_roi.py.
Applies quality thresholds before allowing training to proceed.

Exits:
  0: OK (Ready for training)
  1: BLOCKED (Not enough data, fatal errors)
  2: WARNING (Imbalanced, small classes — training will proceed but flagged)
"""

import argparse
import json
import sys
from pathlib import Path

MIN_TOTAL_IMAGES = 500
MIN_CLASS_IMAGES = 50
MAX_IMBALANCE_RATIO = 10.0
MAX_DUPLICATE_PCT = 5.0
TRAINABLE_CLASSES = {"STATIONARY", "APPROACHING", "DEPARTING", "CROSSING", "ERRATIC"}
IGNORED_CLASSES = {"UNCERTAIN", "uncertain", "FOLLOW", "FOLLOWING"}


def _remove_bad_files(files_meta: list[dict]):
    """Automatically remove corrupt or duplicate files."""
    removed = 0
    for meta in files_meta:
        p = Path(meta["path"])
        if p.exists():
            try:
                p.unlink()
                removed += 1
            except Exception as e:
                print(f"[!] Failed to remove {p}: {e}")
    return removed


def validate(report_path: Path) -> int:
    with open(report_path, encoding="utf-8") as f:
        report = json.load(f)

    print(f"--- Validating Dataset Report: {report_path.name} ---")

    status = 0

    # 1. Handle corrupt images (auto-purge)
    corrupt = report.get("corrupt_images", [])
    if corrupt:
        print(f"[*] Found {len(corrupt)} corrupt/missing images. Auto-removing...")
        removed = _remove_bad_files(corrupt)
        print(f"    -> Removed {removed} files.")
        report["total_images"] -= removed
        # (We don't know exactly which class they belonged to without parsing,
        # but total_images adjustment is enough for the block threshold)

    # 2. Block checks. Training ignores UNCERTAIN samples, so quality gates
    # must use trainable images only.
    raw_classes = report.get("classes", {})
    classes = {
        str(name).upper(): count
        for name, count in raw_classes.items()
        if str(name).upper() in TRAINABLE_CLASSES
    }
    total = sum(classes.values())
    ignored_total = sum(
        count for name, count in raw_classes.items() if str(name).upper() in IGNORED_CLASSES
    )

    if ignored_total:
        print(f"[*] Ignoring {ignored_total} UNCERTAIN/legacy FOLLOW images for training gates.")

    if "review_pending_by_class" not in report:
        print("[!] WARNING: Report predates review-gate fields. Re-run explore_roi.py first.")

    pending_review = report.get("review_pending_by_class", {})
    erratic_pending = int(pending_review.get("ERRATIC", 0))
    if erratic_pending:
        print(f"[❌] BLOCKED: {erratic_pending} ERRATIC samples still need human review.")
        status = 1

    uncertain_pending = int(pending_review.get("UNCERTAIN", 0))
    if uncertain_pending:
        print(f"[*] Review queue: {uncertain_pending} UNCERTAIN samples pending; excluded from training.")

    if total < MIN_TOTAL_IMAGES:
        print(f"[❌] BLOCKED: Trainable images ({total}) < required ({MIN_TOTAL_IMAGES})")
        status = 1
    else:
        print(f"[✅] Trainable images: {total}")

    if status == 1:
        return status  # Fatal, no need to check warnings

    # 3. Warning checks
    if not classes:
        print("[❌] BLOCKED: No trainable classes found.")
        return 1

    counts = list(classes.values())
    max_c = max(counts)
    min_c = min(counts)

    if min_c < MIN_CLASS_IMAGES:
        small_classes = [c for c, count in classes.items() if count < MIN_CLASS_IMAGES]
        print(f"[⚠️] WARNING: Classes with < {MIN_CLASS_IMAGES} images: {small_classes}")
        status = max(status, 2)

    if min_c > 0 and (max_c / min_c) > MAX_IMBALANCE_RATIO:
        print(f"[⚠️] WARNING: Severe class imbalance. Ratio {max_c / min_c:.1f}:1")
        status = max(status, 2)

    dupes = report.get("duplicates", [])
    dupe_pct = (len(dupes) / total) * 100 if total > 0 else 0
    if dupe_pct > MAX_DUPLICATE_PCT:
        print(f"[⚠️] WARNING: High duplicate ratio ({dupe_pct:.1f}% > {MAX_DUPLICATE_PCT}%)")
        status = max(status, 2)
        print(f"    -> Auto-removing {len(dupes)} duplicates...")
        _remove_bad_files(dupes)

    if status == 0:
        print("[✅] Dataset passed all quality checks.")

    return status


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("report", type=Path, help="Path to exploration JSON report")
    args = parser.parse_args()

    if not args.report.exists():
        print(f"[!] Report not found: {args.report}")
        sys.exit(1)

    sys.exit(validate(args.report))
